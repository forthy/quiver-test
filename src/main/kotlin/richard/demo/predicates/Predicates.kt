package richard.demo.predicates

import arrow.core.Predicate


val NonEmptyStringPredicate: Predicate<String> = fun(v: String): Boolean = v.isNotEmpty()

val ShorterThanPredicate: (Int) -> Predicate<String> =
    fun(n: Int): Predicate<String> = fun(v: String): Boolean = v.length in 1..n

val UnregisteredTCPPortPredicate: Predicate<Int> = fun(v: Int): Boolean = v in 0..1023
val RegisteredTCPPortPredicate: Predicate<Int> = fun(v: Int): Boolean = v in 1024 .. 49151

val IPv4Predicate: Predicate<String> = fun(v: String): Boolean =
    """(\b25[0-5]|\b2[0-4][0-9]|\b[01]?[0-9][0-9]?)(\.(25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)){3}""".toRegex()
        .matches(v)

val IPv6Predicate: Predicate<String> =
    fun(v: String): Boolean = """(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))""".toRegex().matches(v)
